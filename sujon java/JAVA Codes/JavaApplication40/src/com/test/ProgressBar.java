    public class Progressing implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent e) {
            int n = pb.getValue();
            if(n<100){
                n++;
                pb.setValue(n);
            }else{
                t.stop();
                JOptionPane.showMessageDialog(null, "Download complete.");
            }
        }
    }

