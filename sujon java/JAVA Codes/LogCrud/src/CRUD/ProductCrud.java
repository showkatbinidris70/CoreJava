    public void storeData(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/loginform", "root","apcl123456");
            PreparedStatement ps=con.prepareStatement("select * from prod12");
            ResultSet rs=ps.executeQuery();
            ResultSetMetaData rsm=rs.getMetaData();
            DefaultTableModel dtm=new DefaultTableModel();
            Object[] columnNames=new Object[rsm.getColumnCount()];
            for(int i=0;i<rsm.getColumnCount();i++){
                columnNames[i]=rsm.getColumnLabel(i+1);
                dtm.addColumn(columnNames[i]);
            }
            while(rs.next()){
                Object[] data =new Object[rsm.getColumnCount()];
                for(int i=0;i<data.length;i++){
                    data[i]=rs.getObject(i+1);
                }
                dtm.addRow(data);
            }table.setModel(dtm);
        }catch(Exception e){
            e.printStackTrace();
        }
    }
