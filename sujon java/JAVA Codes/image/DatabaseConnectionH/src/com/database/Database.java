    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        try {
            String drvName = "com.mysql.jdbc.Driver";
            Class.forName(drvName);
            String cnctnUri = "jdbc:mysql://localhost:3306/mydatabase";
            String userName = "root";
            String password = "apcl123456";
            Connection con = DriverManager.getConnection(cnctnUri, userName, password);
            int i = Integer.parseInt(txtId.getText());
            String a = txtName.getText();
            String b = txtEmail.getText();
            String cb = txtComboboxR.getSelectedItem().toString();
            boolean chtml = checkH.isSelected();
            boolean ccss = checkC.isSelected();
            boolean cjs = checkJs.isSelected();
            boolean csql = checkSql.isSelected();
            boolean cjava = checkjava.isSelected();
            String cskill = "";
            if (checkH.isSelected()) {
                cskill += checkH.getText()+", ";
            }
            if (checkC.isSelected()) {
                cskill += checkC.getText()+", ";
            }
            if (checkJs.isSelected()) {
                cskill += checkJs.getText()+", ";
            }
            if (checkSql.isSelected()) {
                cskill += checkSql.getText()+", ";
            }
            if (checkjava.isSelected()) {
                cskill += checkjava.getText()+", ";
            }
            boolean rm = rbtnF.isSelected();
            boolean rf = rbtnM.isSelected();
            String gender = "";
            if (rbtnF.isSelected()) {
                gender += rbtnF.getText();
            }
            if (rbtnM.isSelected()) {
                gender += rbtnM.getText();
            }
            String date = txtDate.getText();
            int phnno = Integer.parseInt(txtPhone.getText());

            String sqlQuery = "INSERT INTO information(Id, Name, Email, Gender, subject, Phone_no, Date, round)VALUES(" + i + ",'" + a + "','" + b + "', '" + gender + "', '" + cskill + "','" + phnno + "','" + date + "', '" + cb + "');";
            PreparedStatement pstmt = con.prepareStatement(sqlQuery);
            int n = pstmt.executeUpdate();
            System.out.println(n);
            if (n > 0) {
                System.out.println("successfully inserted");
            } else {
                System.out.println("error in insertion");
            }
            con.close();
            pstmt.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnInsertActionPerformed

